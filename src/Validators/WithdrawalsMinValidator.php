namespace Larva\Wallet\Validators;

class WithdrawalsMinValidator
{
    /**
     * @param $attribute
     * @param $value
     * @param $parameters
     * @param $validator
     * @return bool
     */
    public function validate($attribute, $value, $parameters, $validator): bool
    {
        return (int)$value > (int)settings('wallet.withdrawals_min', 100);
    }
}